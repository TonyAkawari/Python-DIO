nome= "Pedro Alvares Cabral de Bragnaça Neto"

# Mostra um unico caractere
print(nome[0])
# Mostra um unico caractere apartir do fim da string
print(nome[-1])
# Mostra a string atrás da seleção
print(nome[:6])
# Mostra a string após da seleção
print(nome[6:])
# Permite a string inteira
print(nome[:])
# Especifica de onde á onde é selecionado
print(nome[14:21])
#Especifica de onde á onde é selecionado e de quanto em quanto é mostrado
print(nome[14:21:2])
# Inverte a string
print(nome[::-1])
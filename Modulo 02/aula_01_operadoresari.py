# Variáveis
produto_1 = 20
produto_2 = 10
# Soma
print(produto_1 + produto_2)
# Subtraçao
print(produto_1 - produto_2)
# Divisão
print(produto_1 / produto_2)
# Divisão que deixa ditero em interger ao invés de float
print(produto_1 // produto_2)
# Multiplicação
print(produto_1 * produto_2)
# Módulo é o resto da divisão: O que resta após o mesmo numero caber no dividido
print(produto_1 % produto_2)
# Exponencial
print(produto_1 ** produto_2)


# Operador de associação: in/not in, para saber se algum conteúdo está ou não em uma variável.
# Se estiver, True. Se não estiver False.

frutas =["limão", "uva"]
print("laranja" in frutas) #false


frutas_1 =["limão", "uva", "laranja"]
print("laranja" in frutas_1) #true
print("limão" not in frutas_1) #false
saldo = 500
print(saldo)


# Atribuiçao simples =
saldo = 200
print(saldo)

# Atribuiçao com adição +=
saldo += 10
print(saldo)

# Atribuiçao com subtração -=
saldo -= 5
print(saldo)

# Atribuiçao com divisão e divisão inteira /= e //=
saldo //= 2
print(saldo)

saldo /= 2
print(saldo)

# Atribuiçao com multiplicação *=
saldo *= 10
print(saldo)

# Atribuiçao com módulo (resto da divisão) %=
saldo %= 4
print(saldo)

# Atribuiçao com exponenciação **=
saldo **= 2
print(saldo)
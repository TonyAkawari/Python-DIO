saldo = 200
saque = 200

# Vao retornar como True ou False
# Equivalente dá True se for, Falso se não
print(saldo == saque)
# Diferente dá True se for, Falso se não
print(saldo != saque)
# Maior que dá True se for, Falso se não
print(saldo > saque)
# Maior ou igual dá True se for, Falso se não
print(saldo >= saque)
# Menor que dá True se for, Falso se não
print(saldo < saque)
# Menor ou igual dá True se for, Falso se não
print(saldo <= saque)